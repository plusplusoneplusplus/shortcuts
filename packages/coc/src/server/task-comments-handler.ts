/** Sub-directory name for task comments storage. */
const COMMENTS_DIR_NAME = 'tasks-comments';

/** Default settings for new comment storage files. */
const DEFAULT_SETTINGS: CommentsStorage['settings'] = {
    showResolved: true,
    highlightColor: '#ffeb3b',
};

