/**
 * Comment on a task file, compatible with extension format.
 */
export interface TaskComment {
    /** Unique identifier (UUID). */
    id: string;
    /** Relative path to task file. */
    filePath: string;
    /** Selection range in the file. */
    selection: {
        startLine: number;
        startColumn: number;
        endLine: number;
        endColumn: number;
    };
    /** The actual selected text. */
    selectedText: string;
    /** User's comment content. */
    comment: string;
    /** Current status. */
    status: 'open' | 'resolved' | 'pending';
    /** ISO timestamp when created. */
    createdAt: string;
    /** ISO timestamp when last updated. */
    updatedAt: string;
    /** Optional author name. */
    author?: string;
    /** Optional tags. */
    tags?: string[];
    /** Optional comment category. */
    category?: string;
    /** Optional anchor for robust location tracking. */
    anchor?: CommentAnchor;
    /** Threaded replies to this comment. */
    replies?: TaskCommentReply[];
    /** AI-generated clarification response. */
    aiResponse?: string;
}

/**
 * A reply to a comment.
 */
export interface TaskCommentReply {
    /** Unique identifier (UUID). */
    id: string;
    /** Author name. */
    author: string;
    /** Reply text content. */
    text: string;
    /** ISO timestamp when created. */
    createdAt: string;
    /** Whether this reply was generated by AI. */
    isAI?: boolean;
}

/**
 * Anchor context for robust comment location tracking.
 */
export interface CommentAnchor {
    selectedText: string;
    contextBefore: string;
    contextAfter: string;
    originalLine: number;
    textHash: string;
}

/**
 * Optional document context supplied by the client to enrich AI prompts.
 */
export interface DocumentContext {
    /** Lines surrounding the selection (excluding the selection itself). */
    surroundingLines?: string;
    /** Nearest markdown heading above the selection. */
    nearestHeading?: string;
    /** All headings in the document. */
    allHeadings?: string[];
    /** Relative path of the source file (overrides comment.filePath if provided). */
    filePath?: string;
}

/**
 * Storage format (matches extension's format).
 */
export interface CommentsStorage {
    comments: TaskComment[];
    settings: {
        showResolved: boolean;
        highlightColor: string;
    };
}

