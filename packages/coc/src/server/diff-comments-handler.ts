/**
 * A reply to a diff comment.
 */
export interface DiffCommentReply {
    /** Unique identifier (UUID). */
    id: string;
    /** Author name. */
    author: string;
    /** Reply text content. */
    text: string;
    /** ISO timestamp when created. */
    createdAt: string;
    /** Whether this reply was generated by AI. */
    isAI?: boolean;
}

/**
 * Storage format for diff comments.
 */
export interface DiffCommentsStorage {
    comments: DiffComment[];
    settings: {
        showResolved: boolean;
    };
}

// ============================================================================
// Constants
// ============================================================================

/** Sub-directory name for diff comments storage. */
const DIFF_COMMENTS_DIR_NAME = 'diff-comments';

/** Default settings for new diff comment storage files. */
const DEFAULT_DIFF_SETTINGS: DiffCommentsStorage['settings'] = {
    showResolved: true,
};

// ============================================================================
// DiffCommentsManager
// ============================================================================

/**
 * Manages diff comments storage and operations.
 * Mirrors TaskCommentsManager but keyed by sha256(context) instead of filePath.
 */
export class DiffCommentsManager {
    private readonly commentsRoot: string;

    /**
     * @param dataDir - Root data directory (e.g. ~/.coc)
     */
    constructor(dataDir: string) {
        this.commentsRoot = path.join(dataDir, DIFF_COMMENTS_DIR_NAME);
    }

    /**
     * Derive a storage key from a diff context.
     *
     * Fields are concatenated without a separator — clients must use the same
     * concatenation order to produce matching keys.
     *
     * Normal diff:    sha256(repositoryId + oldRef + newRef + filePath)
     * Working-tree:   sha256(repositoryId + filePath + 'working-tree')
     *                 (oldRef is omitted; 'working-tree' sentinel is appended)
     */
    hashContext(ctx: DiffCommentContext): string {
        if (ctx.newRef === 'working-tree') {
            return crypto
                .createHash('sha256')
                .update(ctx.repositoryId + ctx.filePath + 'working-tree')
                .digest('hex');
        }
        return crypto
            .createHash('sha256')
            .update(ctx.repositoryId + ctx.oldRef + ctx.newRef + ctx.filePath)
            .digest('hex');
    }

    /** Get comments directory for a workspace. */
    private getWorkspaceDir(wsId: string): string {
        return path.join(this.commentsRoot, wsId);
    }

    /** Get storage file path for a storage key. */
    private getStorageFile(wsId: string, storageKey: string): string {
        return path.join(this.getWorkspaceDir(wsId), `${storageKey}.json`);
    }

    /** Ensure workspace directory exists. */
    private ensureWorkspaceDir(wsId: string): void {
        const dir = this.getWorkspaceDir(wsId);
        if (!fs.existsSync(dir)) {
            fs.mkdirSync(dir, { recursive: true });
        }
    }

    /** Read all comments for a storage key. */
    async getComments(wsId: string, storageKey: string): Promise<DiffComment[]> {
        const file = this.getStorageFile(wsId, storageKey);
        if (!fs.existsSync(file)) {
            return [];
        }
        try {
            const content = await fs.promises.readFile(file, 'utf8');
            const storage: DiffCommentsStorage = JSON.parse(content);
            return storage.comments || [];
        } catch {
            return [];
        }
    }

    /** Write comments to storage atomically (write to temp file then rename). */
    async writeComments(wsId: string, storageKey: string, comments: DiffComment[]): Promise<void> {
        this.ensureWorkspaceDir(wsId);
        const file = this.getStorageFile(wsId, storageKey);
        const storage: DiffCommentsStorage = {
            comments,
            settings: DEFAULT_DIFF_SETTINGS,
        };
        const tempFile = `${file}.tmp`;
        try {
            await fs.promises.writeFile(tempFile, JSON.stringify(storage, null, 2), 'utf8');
            await fs.promises.rename(tempFile, file);
        } catch (error) {
            // Clean up temp file on error
            try { await fs.promises.unlink(tempFile); } catch { /* ignore */ }
            throw error;
        }
    }

    /**
     * Add a new comment.
     * Sets `ephemeral: true` when `ctx.newRef === 'working-tree'`.
     */
    async addComment(
        wsId: string,
        ctx: DiffCommentContext,
        commentData: Omit<DiffComment, 'id' | 'createdAt' | 'updatedAt' | 'ephemeral'>
    ): Promise<DiffComment> {
        const storageKey = this.hashContext(ctx);
        const comments = await this.getComments(wsId, storageKey);
        const now = new Date().toISOString();
        const newComment: DiffComment = {
            ...commentData,
            id: crypto.randomUUID(),
            createdAt: now,
            updatedAt: now,
            ...(ctx.newRef === 'working-tree' ? { ephemeral: true } : {}),
        };
        comments.push(newComment);
        await this.writeComments(wsId, storageKey, comments);
        return newComment;
    }

    /** Update an existing comment. Returns null if not found. */
    async updateComment(
        wsId: string,
        storageKey: string,
        id: string,
        updates: Partial<Omit<DiffComment, 'id' | 'createdAt'>>
    ): Promise<DiffComment | null> {
        const comments = await this.getComments(wsId, storageKey);
        const index = comments.findIndex(c => c.id === id);
        if (index === -1) {
            return null;
        }
        comments[index] = {
            ...comments[index],
            ...updates,
            id: comments[index].id,
            createdAt: comments[index].createdAt,
            updatedAt: new Date().toISOString(),
        };
        await this.writeComments(wsId, storageKey, comments);
        return comments[index];
    }

    /** Delete a comment. Returns false if not found. */
    async deleteComment(wsId: string, storageKey: string, id: string): Promise<boolean> {
        const comments = await this.getComments(wsId, storageKey);
        const filtered = comments.filter(c => c.id !== id);
        if (filtered.length === comments.length) {
            return false;
        }
        await this.writeComments(wsId, storageKey, filtered);
        return true;
    }

    /** Get a single comment by ID. Returns null if not found. */
    async getComment(wsId: string, storageKey: string, id: string): Promise<DiffComment | null> {
        const comments = await this.getComments(wsId, storageKey);
        return comments.find(c => c.id === id) || null;
    }

    /** Add a reply to a comment. Returns null if comment not found. */
    async addReply(
        wsId: string,
        storageKey: string,
        id: string,
        replyData: { author: string; text: string; isAI?: boolean }
    ): Promise<DiffCommentReply | null> {
        const comments = await this.getComments(wsId, storageKey);
        const index = comments.findIndex(c => c.id === id);
        if (index === -1) return null;

        const reply: DiffCommentReply = {
            id: crypto.randomUUID(),
            author: replyData.author,
            text: replyData.text,
            createdAt: new Date().toISOString(),
            isAI: replyData.isAI,
        };

        if (!comments[index].replies) {
            comments[index].replies = [];
        }
        (comments[index].replies as DiffCommentReply[]).push(reply);
        comments[index].updatedAt = new Date().toISOString();
        await this.writeComments(wsId, storageKey, comments);
        return reply;
    }

    /**
     * Get comment counts for all storage keys in a workspace.
     * Returns a map of storageKey → comment count.
     */
    async getCommentCounts(wsId: string): Promise<Record<string, number>> {
        const wsDir = this.getWorkspaceDir(wsId);
        if (!fs.existsSync(wsDir)) {
            return {};
        }
        const counts: Record<string, number> = {};
        let entries: string[];
        try {
            entries = await fs.promises.readdir(wsDir);
        } catch {
            return {};
        }
        for (const entry of entries) {
            if (!entry.endsWith('.json')) continue;
            try {
                const content = await fs.promises.readFile(path.join(wsDir, entry), 'utf8');
                const storage: DiffCommentsStorage = JSON.parse(content);
                const comments = storage.comments || [];
                const key = entry.slice(0, -5); // strip ".json"
                counts[key] = comments.length;
            } catch {
                // Skip corrupted files
            }
        }
        return counts;
    }

    /**
     * List all comments in a workspace across all storage files.
     */
    async listAllComments(wsId: string): Promise<DiffComment[]> {
        const wsDir = this.getWorkspaceDir(wsId);
        if (!fs.existsSync(wsDir)) {
            return [];
        }
        let entries: string[];
        try {
            entries = await fs.promises.readdir(wsDir);
        } catch {
            return [];
        }
        const all: DiffComment[] = [];
        for (const entry of entries) {
            if (!entry.endsWith('.json')) continue;
            try {
                const content = await fs.promises.readFile(path.join(wsDir, entry), 'utf8');
                const storage: DiffCommentsStorage = JSON.parse(content);
                all.push(...(storage.comments || []));
            } catch {
                // Skip corrupted files
            }
        }
        return all;
    }
}

// ============================================================================
// Validation Helpers
// ============================================================================

/** Validate workspace ID to prevent path traversal. */
function isValidWorkspaceId(wsId: string): boolean {
    return /^[a-zA-Z0-9_-]+$/.test(wsId) && !wsId.includes('..');
}

/** Validate storage key (SHA-256 hex, 64 chars). */
function isValidStorageKey(key: string): boolean {
    return /^[0-9a-f]{64}$/.test(key);
}

/** Validate that a diff comment context object has all required fields. */
function isValidContext(ctx: any): ctx is DiffCommentContext {
    return (
        ctx &&
        typeof ctx === 'object' &&
        typeof ctx.repositoryId === 'string' &&
        typeof ctx.oldRef === 'string' &&
        typeof ctx.newRef === 'string' &&
        typeof ctx.filePath === 'string'
    );
}

// ============================================================================
// URL Patterns
// ============================================================================

// /api/diff-comment-counts/:wsId
const countsPattern = /^\/api\/diff-comment-counts\/([a-zA-Z0-9_-]+)$/;

// /api/diff-comments/:wsId  (list all / create)
const collectionPattern = /^\/api\/diff-comments\/([a-zA-Z0-9_-]+)$/;

// /api/diff-comments/:wsId/:storageKey
const storageKeyPattern = /^\/api\/diff-comments\/([a-zA-Z0-9_-]+)\/([0-9a-f]{64})$/;

// /api/diff-comments/:wsId/:storageKey/:id
const itemPattern = /^\/api\/diff-comments\/([a-zA-Z0-9_-]+)\/([0-9a-f]{64})\/([0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12})$/;

// /api/diff-comments/:wsId/:storageKey/:id/replies
const replyPattern = /^\/api\/diff-comments\/([a-zA-Z0-9_-]+)\/([0-9a-f]{64})\/([0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12})\/replies$/;

// /api/diff-comments/:wsId/:storageKey/:id/ask-ai
const askAiPattern = /^\/api\/diff-comments\/([a-zA-Z0-9_-]+)\/([0-9a-f]{64})\/([0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12})\/ask-ai$/;

