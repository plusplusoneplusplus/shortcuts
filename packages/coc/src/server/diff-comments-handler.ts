/**
 * Diff Comments REST API Handler
 *
 * HTTP API routes for CRUD operations on git diff view comments.
 * Stores comments in JSON files under the CoC data directory.
 *
 * Storage layout:
 *   {dataDir}/diff-comments/{workspaceId}/{sha256(repoId+oldRef+newRef+filePath)}.json
 *
 * For working-tree diffs (newRef === 'working-tree'), the storage key is
 *   sha256(repoId+filePath+'working-tree') and every comment is ephemeral.
 *
 * No VS Code dependencies — uses only Node.js built-in modules.
 * Cross-platform compatible (Linux/Mac/Windows).
 */

import * as crypto from 'crypto';
import * as fs from 'fs';
import * as path from 'path';
import { sendJSON, sendError, parseBody } from '@plusplusoneplusplus/coc-server';
import type { Route, ProcessWebSocketServer } from '@plusplusoneplusplus/coc-server';
import type { DiffComment, DiffCommentContext } from '@plusplusoneplusplus/pipeline-core';
import {
    DEFAULT_AI_COMMANDS,
    type AICommand,
    buildPromptFromContext,
    type PromptContext,
} from '@plusplusoneplusplus/pipeline-core';
import type { ProcessStore } from '@plusplusoneplusplus/pipeline-core';
import type { MultiRepoQueueExecutorBridge } from './multi-repo-executor-bridge';

// ============================================================================
// Types
